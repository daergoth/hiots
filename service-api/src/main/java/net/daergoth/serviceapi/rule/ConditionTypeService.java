package net.daergoth.serviceapi.rule;

/**
 * Describes Condition types for th Rule service.
 *
 */
public enum ConditionTypeService {
	
	/**
	 * Equals condition.
	 */
	EQ,
	
	/**
	 * Greater than condition.
	 */
	GT,
	
	/**
	 * Less than condition.
	 */
	LT,
	
	/**
	 * Greater than or equals condition.
	 */
	GE,
	
	/**
	 * Less than or equals condition.
	 */
	LE
}
