package net.daergoth.coreapi.rule;

/**
 * Describes all {@code Condition} types.
 * 
 * @see net.daergoth.coreapi.rule.ConditionDTO
 */
public enum ConditionTypeCore {
	
	/**
	 * Equals type.
	 */
	EQ,
	
	/**
	 * Greater than type.
	 */
	GT,
	
	/**
	 * Less than type.
	 */
	LT,
	
	/**
	 * Greater than or equal type.
	 */
	GE,
	
	/**
	 * Less than or equal type.
	 */
	LE
}
